#include "3-calc.h"

/**
 * op_add - returns the sum of a and b
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the diff of a and b
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of a and b
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the result of the div of a by b
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the sum of a and b
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
